import xadmin

########################################################################################################################
## 自建模块导入
########################################################################################################################
from .models import *


########################################################################################################################
## 注册项目分类到后台
########################################################################################################################
class ProjectCategoryAdmin(object):
    list_display = ['name', 'dev_group', 'is_use']

xadmin.site.register(ProjectCategory, ProjectCategoryAdmin)


########################################################################################################################
## 注册项目到后台
########################################################################################################################
class ProjectAdmin(object):
    list_display = ['name', 'cate', 'is_use']

xadmin.site.register(Project, ProjectAdmin)


########################################################################################################################
## 注册项目详情到后台
########################################################################################################################
class ProjectInfoAdmin(object):
    list_display = ['project', 'ask_user', 'op_user', 'dba_user']

xadmin.site.register(ProjectInfo, ProjectInfoAdmin)


########################################################################################################################
## 注册安装文档标签到后台
########################################################################################################################
class InstallDocTagAdmin(object):
    list_display = ['name']

xadmin.site.register(InstallDocTag, InstallDocTagAdmin)


########################################################################################################################
## 注册安装文档到后台
########################################################################################################################
class InstallDocAdmin(object):
    list_display = ['doc_title']

xadmin.site.register(InstallDoc, InstallDocAdmin)










