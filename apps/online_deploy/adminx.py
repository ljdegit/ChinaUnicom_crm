import xadmin

########################################################################################################################
## 自建模块导入
########################################################################################################################
from .models import *


########################################################################################################################
## 注册服务到后台
########################################################################################################################
class ProductionServiceAdmin(object):
    list_display = ['name']

xadmin.site.register(ProductionService,ProductionServiceAdmin)


########################################################################################################################
## 注册中间件到后台
########################################################################################################################
class MiddlewareRecordAdmin(object):
    list_display = ['pro', 'service']

xadmin.site.register(MiddlewareRecord,MiddlewareRecordAdmin)


########################################################################################################################
## 注册发布记录到后台
########################################################################################################################
class DeployRecordAdmin(object):
    list_display = ['pro', 'service']

xadmin.site.register(DeployRecord,DeployRecordAdmin)








